//Criar um programa com uma condicional composta para avaliar o desempenho escolar entre 'execelente', 'otimo', 'bom', 'regular' ou 'ruim'. Dependendo da nota

let nota = 8; 

if (nota >= 9) {
    console.log("Excelente");
} else if (nota >= 8) {
    console.log("Ótimo");
} else if (nota >= 7) {
    console.log("Bom");
} else if (nota >= 5) {
    console.log("Regular");
} else {
    console.log("Ruim");
}